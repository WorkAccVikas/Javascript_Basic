// TOPIC : Arithmetic Operators

let a = 15;
let b = 2;

console.log(`a + b = ${a + b}`); // 17
console.log(`a - b = ${a - b}`); // 13
console.log(`a * b = ${a * b}`); // 30
console.log(`a / b = ${a / b}`); // 7.5
console.log(`a % b = ${a % b}`); // 1
console.log(`a ** b = ${a ** b}`); // 225
console.log("++a = ", ++a); // 16
console.log("a++ = ", a++); // 16
console.log("--a = ", --a); // 16
console.log("a-- = ", a--); // 16
console.log("a = ", a); // 15